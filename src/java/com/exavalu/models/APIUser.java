package com.exavalu.models;

import com.exavalu.services.APIService;
import com.opensymphony.xwork2.ActionContext;
import com.opensymphony.xwork2.ActionSupport;
import java.io.Serializable;
import java.util.ArrayList;
import java.util.Map;
import org.apache.struts2.dispatcher.ApplicationMap;
import org.apache.struts2.dispatcher.SessionMap;
import org.apache.struts2.interceptor.ApplicationAware;
import org.apache.struts2.interceptor.SessionAware;
import org.json.simple.parser.ParseException;

/**
 *
 * @author RISHAV DUTTA
 */
public class APIUser extends ActionSupport implements ApplicationAware, SessionAware, Serializable {

    private SessionMap<String, Object> sessionMap = (SessionMap) ActionContext.getContext().getSession();

    private ApplicationMap map = (ApplicationMap) ActionContext.getContext().getApplication();

    @Override
    public void setApplication(Map<String, Object> application) {
        map = (ApplicationMap) application;
    }

    @Override
    public void setSession(Map<String, Object> session) {
        sessionMap = (SessionMap) session;
    }

    private String id, userId, body, title;

    public String getDataFromAPI() throws ParseException {
        sessionMap.clear();
        String result = "FAILURE";
        ArrayList apiUsers = APIService.consumeDataFromAPI();
        boolean res = APIService.insertDataInDB(apiUsers);
        if (res) {
            String msg = "Data Added in the DataBase!!!";
            sessionMap.put("Success_Msg", msg);
            result = "SUCCESS";
        } else {
            String errorMsg = "Data Already Present in the DataBase!!!";
            sessionMap.put("Error_Msg", errorMsg);
        }

        return result;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
